package I_PARCIAL.EstructurasRepetitiva;
public class EstructuraRepetitivaFor {
    public static void main(String[] args) {
        for(int i=1; i<11; i++){
            System.out.println("5 x "+i+" = "+5*i);
            
        }
    }
    
}
